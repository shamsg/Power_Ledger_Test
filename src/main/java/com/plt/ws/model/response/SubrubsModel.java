package com.plt.ws.model.response;

public class SubrubsModel {

	private String id;
	private String subrubName;
	private String subrubZipCode;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getSubrubName() {
		return subrubName;
	}
	public void setSubrubName(String subrubName) {
		this.subrubName = subrubName;
	}
	public String getSubrubZipCode() {
		return subrubZipCode;
	}
	public void setSubrubZipCode(String subrubZipCode) {
		this.subrubZipCode = subrubZipCode;
	}
	
}
